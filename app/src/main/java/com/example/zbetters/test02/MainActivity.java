    public void showMessage(View v) {
        EditText tbxname = (EditText) findViewById(R.id.txtName);
        TextView tbxhello = (TextView) findViewById(R.id.showName);
        if(tbxname.getText().toString().trim().length() == 0) {
            Toast.makeText(this, "Enter a name!", Toast.LENGTH_LONG).show();
        }
        else {
            tbxhello.setText("Hello, " + tbxname.getText() + "!");
        }
    }

